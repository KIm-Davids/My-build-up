public class ForCounter {
	public static void main(String[] args) {
	System.out.println("Numbers from 1 - 100");
		for (int counter = 1; counter <= 100; counter++) 
		{
		System.out.println("\n" + counter);

		}
		
		for (int counter = 1; counter <= 100; counter++)
			{
		System.out.printf(" %d ", counter);
			}

			System.out.println();
	
	}
}	